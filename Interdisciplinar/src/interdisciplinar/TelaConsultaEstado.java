    private boolean selecionouUmaLinha() {

        _linhaSelecionada = tbEstados.getSelectedRow();
        if (_linhaSelecionada == -1 || _estados == null || _estados.size() == 0) {
            return false;
        }

        return true;
    }

    private int obterIdSelecionado() {

        int linhaSelecionada = tbEstados.getSelectedRow();
        Object valor = tbEstados.getModel().getValueAt(linhaSelecionada, 0);
        int id = (int) valor;
        return id;

    }

    private void excluirEstado(int id) {

        boolean excluiu = _banco.excluirEstado(id);
        if (excluiu) {
            Util.mensagemDeAlerta("Veiculo excluido com sucesso", this);
            _tabela.montarEstados(tbEstados);
            return;
        }

        Util.mensagemDeAlerta("Ocorreu um erro ao tentar excluir o veiculo", this);

    }


