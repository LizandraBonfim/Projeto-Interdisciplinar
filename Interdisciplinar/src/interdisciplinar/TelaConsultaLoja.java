    private void carregaTabelaDeLojas() {
        _lojas = _banco.listarLoja();
        _tabela.montarLojas(tbLojas, _lojas);
    }

    private boolean selecionouUmaLinha() {

        _linhaSelecionada = tbLojas.getSelectedRow();
        if (_linhaSelecionada == -1 || _lojas == null || _lojas.size() == 0) {
            return false;
        }

        return true;
    }

