    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if (!selecionouUmaLinha()) {
            return;
        }

        int id = obterIdSelecionado();
        boolean querExcluir = Util.mensagemDeConfirmacao("Deseja realmente excluir esse registro ?", this);
        if (querExcluir) {
            excluirLoja(id);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private int obterIdSelecionado() {

        int linhaSelecionada = tbLojas.getSelectedRow();
        Object valor = tbLojas.getModel().getValueAt(linhaSelecionada, 0);
        int id = (int) valor;
        return id;

    }

    private void excluirLoja(int id) {

        boolean excluiu = _banco.excluirLoja(id);
        if (excluiu) {
            Util.mensagemDeAlerta("Loja excluida com sucesso", this);
            _tabela.montarLojas(tbLojas);
            return;
        }

        Util.mensagemDeAlerta("Ocorreu um erro ao tentar excluir a loja", this);

    }

