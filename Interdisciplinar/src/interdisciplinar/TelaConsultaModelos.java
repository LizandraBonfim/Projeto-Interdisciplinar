    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        if (!selecionouUmaLinha()) {
            return;
        }        
        
        int id = obterIdSelecionado();
        new TelaCadastroModelos(id).show();
        _tabela.montarModelos(tbModelos);

    }//GEN-LAST:event_btnEditarActionPerformed

    private boolean selecionouUmaLinha() {

        _linhaSelecionada = tbModelos.getSelectedRow();
        if (_linhaSelecionada == -1 || _modelos == null || _modelos.size() == 0) {
            return false;
        }

        return true;
    }

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if (!selecionouUmaLinha()) {
            return;
        }

        int id = obterIdSelecionado();
        boolean querExcluir = Util.mensagemDeConfirmacao("Deseja realmente excluir esse registro ?", this);
        if (querExcluir) {
            excluirModelo(id);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

    private int obterIdSelecionado() {

        int linhaSelecionada = tbModelos.getSelectedRow();
        Object valor = tbModelos.getModel().getValueAt(linhaSelecionada, 0);
        int id = (int) valor;
        return id;

    }
    
    private void excluirModelo(int id){
        
        boolean excluiu = _banco.excluirMarcaModelo(id);
        if (excluiu){
            Util.mensagemDeAlerta("Veiculo excluido com sucesso", this);
            carregarTabelaDeModelos();
            return;            
        }        
        
        Util.mensagemDeAlerta("Ocorreu um erro ao tentar excluir o veiculo", this);
        
    }

